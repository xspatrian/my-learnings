# 1)Upper Case :
a = "Hello, World!"
print(a.upper())


# 2)Lower Case :
a = "Hello, World!"
print(a.lower())

# 3)Remove Whitespace : 
# Whitespace is the space before and/or after the actual text, 
# and very often you want to remove this space
a = " Hello, World! "
print(a.strip()) # returns "Hello, World!"


# 4)Replace String :
a = "Hello, World!"
print(a.replace("H", "J"))

# 5)Split String :
a = "Hello, World how are you / on my '!"
b = a.split("/")
print(b)


